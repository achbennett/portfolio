            <h3 class="panel-title">What does the Online Express Forms plugin do?</h3>
        </div>
        <div class="panel-body">
            <p>The Online Express Forms plugin makes it easy to catalog your various embed code snippets and display them in various capacities on your WordPress website. Using this plugin, you can insert your Donation, Membership, and Events forms on any page, post, or widget using a simple shortcode.</p>
            <p><strong>Features:</strong></p>
            <ul class="bulleted-list">
                <li>Shortcodes to represent each Online Express embed code</li>
                <li>Editor button to allow you to easily add an Online Express form to your posts and pages</li>
                <li>Social sharing capabilities on the various confirmation screens</li>
            </ul>
        </div>
    </div>

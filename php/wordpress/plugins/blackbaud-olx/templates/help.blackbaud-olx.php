        <div class="panel-header">
            <h3 class="panel-title">How do I add an Online Express Form to my WordPress site?</h3>
        </div>
        <div class="panel-body">
            <h4>Adding the form to the WordPress Dashboard</h4>
            <ol class="numbered-list">
                <li>Using the Online Express platform, configure your form as needed.</li>
                <li>Copy the Embed Code to your clipboard (Ctrl+C or Cmd+C).</li>
                <li>On your WordPress Dashboard, click on <em>Online Express Forms > Add New</em>.</li>
                <li>Paste the embed code into the "Embed Code" field (Ctrl+V or Cmd+V), and fill out the rest of the form as needed.</li>
            </ol>
            <h4>Adding the form to a post or page</h4>
            <ol class="numbered-list">
                <li>Edit the post or page that will display an Online Express Form (or, create a new post or page).</li>
                <li>Determine where you want your form to be placed by clicking somewhere in the main Content Area. (The form can appear above or below other paragraphs or images, but it must be on its own line.)</li>
                <li>Next, click on the Content Area's "OLX" editor button (<img src="../wp-content/plugins/blackbaud-olx/public_html/img/help-olx-button-icon.png">) and select a form from the dropdown.</li>
                <li>You should now see a <a href="https://en.support.wordpress.com/shortcodes/" target="_blank">WordPress Shortcode</a> in the Content Area representing your Online Express Form.</li>
                <li>Save the post/page and preview it, making sure that the form appears correctly.</li>
            </ol>
        </div>
